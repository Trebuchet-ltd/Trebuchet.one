// about
$("#about").mouseenter(function(){
    $("#nav-about").css("color", "green");
    $("#nav-about").css("font-weight", 900);
});
$("#about").mouseleave(function(){
    $("#nav-about").css("color", "white");
    $("#nav-about").css("font-weight", 500);
});

